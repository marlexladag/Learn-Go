// Day 1, Exercise 1: Hello World
// This is your first Go program!
//
// Key concepts:
// - Every Go file starts with a package declaration
// - 'main' package is special - it's the entry point
// - 'import' brings in other packages
// - 'func main()' is where execution begins

package main

import "fmt"

func main() {
	// fmt.Println prints text followed by a newline
	fmt.Println("Hello, World!")
	fmt.Println("Welcome to Go!")
}

// TO RUN: go run 01_hello.go
